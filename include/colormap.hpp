inline 
vtkLookupTable* vtkSetColorTable(vtkLookupTable* colorTable,const int colormapIndex)
{
    colorTable->SetNumberOfColors(256);
    float (*colormap)[3] =nullptr;
    switch(colormapIndex)
    {
        case Jet:
            colormap=JetColormap;
            break;
        case Cool:
            colormap=CoolColormap;
            break;
        case Hot:
            colormap=HotColormap;
            break;
		case Parula:
			colormap = ParulaColormap;
			break;
        case Hsv:
            colormap=HsvColormap;
            break;
        case Spring:
            colormap=SpringColormap;
            break;
        case Summer:
            colormap=SummerColormap;
            break;
        case Autumn:
            colormap=AutumnColormap;
            break;
        case Winter:
            colormap=WinterColormap;
            break;
        case Bone:
            colormap=BoneColormap;
            break;
        case Copper:
            colormap=CopperColormap;
            break;
        case Pink:
            colormap=PinkColormap;
            break;
        default:
            std::cerr<<"not implent colormap!"<<std::endl;
            exit(EXIT_FAILURE);
    }
    for(unsigned int i=0;i<256;++i)
    {
        colorTable->SetTableValue(i,colormap[i][0],colormap[i][1],colormap[i][2]);
    }
    colorTable->Build();
    return colorTable;
}

